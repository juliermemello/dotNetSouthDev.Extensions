namespace dotNetSouthDev.Extensions.Math;

public static partial class MathExtensions
{
    public static int Mod(this short leftSide, short rightSide)
    {
        int result = leftSide % rightSide;
        return result;
    }

    public static int Mod(this short leftSide, int rightSide)
    {
        int result = leftSide % rightSide;
        return result;
    }

    public static long Mod(this short leftSide, long rightSide)
    {
        long result = leftSide % rightSide;
        return result;
    }

    public static int Mod(this int leftSide, short rightSide)
    {
        int result = leftSide % rightSide;
        return result;
    }

    public static int Mod(this int leftSide, int rightSide)
    {
        int result = leftSide % rightSide;
        return result;
    }

    public static long Mod(this int leftSide, long rightSide)
    {
        long result = leftSide % rightSide;
        return result;
    }

    public static long Mod(this long leftSide, short rightSide)
    {
        long result = leftSide % rightSide;
        return result;
    }

    public static long Mod(this long leftSide, int rightSide)
    {
        long result = leftSide % rightSide;
        return result;
    }

    public static long Mod(this long leftSide, long rightSide)
    {
        long result = leftSide % rightSide;
        return result;
    }
}
